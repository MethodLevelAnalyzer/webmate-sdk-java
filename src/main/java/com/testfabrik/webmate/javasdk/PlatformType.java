/**
 * This interface includes all platforms available in webmate.
 */
public interface PlatformType {

    /**
     * Android mobile OS.
     */
    String ANDROID = "Android";

    /**
     * iOS mobile OS.
     */
    String IOS = "iOS";

    /**
     * Windows OS.
     */
    String WINDOWS = "WINDOWS";

    /**
     * MacOS.
     */
    String MACOS = "MACOS";

    /**
     * Ubuntu OS.
     */
    String UBUNTU = "UBUNTU";

}
